//Bài 2
var width = 10;
var height = 20;

/**
 * width: Chiều rộng của 1 hình chữ nhật
 * height: Chiều dài của hình chữ nhật
 * Yêu cầu: 
 * - Khai báo biến s là diện tích của hình chữ nhật này.
 * - Khai báo biến p là chu vi của hình chữ nhật này.
 */
 
// viết code ở đây.
var p = (width + height) * 2;
var s = width * height;
console.log(p);
console.log(s);

