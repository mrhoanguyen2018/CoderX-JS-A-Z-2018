/**
 * r: Bán kính của 1 hình tròn
 * s: Diện tích của hình tròn đó
 * pi: 3.14
 * Yêu cầu: 
 * - Khai báo biến p là chu vi của hình tròn này.
 * - Khao báo biến s là diện tích của hình tròn này.
 */
// viết code ở đây.
var r = 4;
var p = r * 2 * 3.14;
var s = r * r * 3.14;
